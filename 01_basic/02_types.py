# Python tiene varios tipo de datos
# int: enteros
# float: flotantes
# str: cadenas de texto
# bool: booleanos
# list: listas
# tuple: tuplas
# dict: diccionarios
# set: conjuntos
# None: None

"""
Asi puedes hacer una cadena de texto multilinea para documentar tu codigo
"""

print(type(10))
print(type(-5))
print(type(5234523452412343130))


# float: Numero con decimales
print(type(10.5))
print(type(-5.5))
print(type(1e3))

# Ausencia de valor
print(type(None))

# Numeros complejos
print(type(1j))
print(type(1+2j))
print(type(1-2j))
print(type(1+2j)) 